export type TranslationCopy = typeof translations.en;

const resolvePath = (obj: Record<string, any>, path: string) => {
  return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj);
};

export const translate = (language: Language, path: string, params?: Record<string, string | number>) => {
  const copy = translations[language] ?? translations.en;
  const value = resolvePath(copy as Record<string, any>, path) ?? resolvePath(translations.en as Record<string, any>, path);
  if (typeof value !== 'string') return '';
  if (!params) return value;
  return Object.entries(params).reduce((acc, [key, val]) => acc.replace(`{${key}}`, String(val)), value);
};

export const getCopy = (language: Language): TranslationCopy => {
  return (translations[language] ?? translations.en) as TranslationCopy;
};
